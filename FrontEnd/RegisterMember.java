    /**
     * @param args the command line arguments
     */
    private void registerMember(LocalDate registrationDate) {
        String classId = IdCadd1.getText().trim();
        String memberId = IdMadd.getText().trim();
        //  LocalDate registrationDate = LocalDate.now();
        if (classId.isEmpty() || memberId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Some Fields are empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        trainer.registerMemberForClass(memberId, classId, registrationDate);
    }

