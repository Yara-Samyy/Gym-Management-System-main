    private void RegBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegBtnActionPerformed
        // TODO add your handling code here:
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Please select a registration date.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        LocalDate date = jDateChooser1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        registerMember(date);
        new trainerRole().setVisible(true);
        this.dispose();

    }//GEN-LAST:event_RegBtnActionPerformed

