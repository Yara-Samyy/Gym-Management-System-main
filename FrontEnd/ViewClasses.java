     private void fillTable() {
        String[] columns = {"Class Id", "Class Name", "Trainer Id", "Duration", "Max Participants"};

        ArrayList<Record> classes = trainerRole.getListOfClasses();
        DefaultTableModel model = new DefaultTableModel(columns, 0);  // 0 specifies initially empty

        for (Record c : classes) {
            String line = c.lineRepresentation();
            String[] rowData = line.split(",");

            // Ensure rowData matches expected column count before adding
            if (rowData.length == columns.length) {
                model.addRow(rowData);
            } else {
                JOptionPane.showMessageDialog(null, "Error: Unexpected data format in line: " + line, "Message", JOptionPane.ERROR_MESSAGE);
            }
        }

        // Apply the model to the table
        jTable1.setModel(model);
    }

